      <center><h2>Colleen Hoover</h2></center><br>
      <p>Colleen Hoover es una autora estadounidense nacida el 11 de diciembre de 1979 en Sulphur Springs, Texas. Se ha convertido en una de las autoras más populares y exitosas en el ámbito de la literatura contemporánea, especialmente en el género de romance. Antes de dedicarse por completo a la escritura, Hoover trabajó como asistente social y como lectora ávida, lo que le permitió desarrollar su pasión por contar historias.

<br>Su carrera literaria despegó en 2012 con la publicación de su primera novela, Slammed, que se convirtió en un éxito inesperado. La obra, que explora temas de amor, pérdida y superación personal, fue bien recibida por los lectores y rápidamente ganó popularidad en plataformas digitales. Desde entonces, ha publicado una serie de novelas exitosas, entre las que se destacan Hopeless, Confess, It Ends with Us y Verity. Sus libros suelen abordar temas emocionales y complejos, como la salud mental, el abuso y las relaciones, resonando profundamente con sus lectores.

<br>Colleen Hoover ha sido reconocida con numerosos premios literarios y ha aparecido en listas de bestsellers del New York Times en múltiples ocasiones. Su estilo de escritura, que combina narrativas emocionales con giros inesperados, ha atraído a una amplia audiencia, convirtiéndola en una figura influyente en el mundo literario. Además, Hoover es muy activa en las redes sociales, donde comparte su proceso creativo y conecta con sus seguidores.

<br>En 2021, It Ends with Us alcanzó un nuevo nivel de popularidad, especialmente entre el público joven, lo que llevó a un aumento significativo en su base de lectores. La obra fue inspirada por experiencias personales y aborda temas delicados, lo que ha generado conversaciones sobre relaciones y empoderamiento.

<br>Colleen Hoover es conocida no solo por su habilidad para contar historias conmovedoras, sino también por su enfoque en temas sociales y su deseo de dar voz a experiencias que a menudo son ignoradas. Su legado como autora continúa creciendo, y sus obras siguen tocando el corazón de muchos, lo que la ha consolidado como una de las autoras más queridas de su generación. </p><br><br>
