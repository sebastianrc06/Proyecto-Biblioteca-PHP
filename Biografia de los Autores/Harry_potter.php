      <center><h2>J.K Rowling</h2></center><br>
      <p>J.K. Rowling, nacida como Joanne Rowling el 31 de julio de 1965 en Yate, Gloucestershire, Inglaterra, es una autora británica célebre por su serie de libros Harry Potter, que ha cautivado a millones de lectores en todo el mundo. Desde joven, mostró un interés por la escritura, completando su primer relato a los seis años.

<br>Después de estudiar en la Universidad de Exeter, Rowling trabajó en varios empleos, incluyendo como secretaria y profesora de inglés en Portugal. En 1990, durante un viaje en tren, tuvo la idea de un niño que descubre que es un mago, lo que originó la serie Harry Potter. El primer libro, Harry Potter y la piedra filosofal, fue publicado en 1997 y rápidamente se convirtió en un fenómeno global, dando lugar a siete libros en total, que han vendido más de 500 millones de copias y han sido traducidos a más de 80 idiomas.

<br>A pesar de su éxito, Rowling enfrentó desafíos personales, incluyendo pobreza y depresión. Su capacidad para superar estos obstáculos ha resonado con muchos de sus lectores. Además de la serie Harry Potter, ha escrito obras para adultos, como El canto del cuco, bajo el seudónimo de Robert Galbraith.

<br>Rowling también es conocida por su trabajo filantrópico, habiendo donado grandes sumas a organizaciones benéficas, y ha creado su propia fundación. Sin embargo, su vida pública ha estado marcada por controversias, especialmente por sus comentarios sobre temas de género, generando tanto críticas como defensores.

<br>El legado de J.K. Rowling es significativo, no solo por su éxito comercial, sino también por su impacto en la literatura contemporánea. Su habilidad para crear un mundo mágico lleno de personajes entrañables ha dejado una huella perdurable en la literatura infantil y juvenil, inspirando a nuevas generaciones a explorar el placer de la lectura. </p><br><br>
