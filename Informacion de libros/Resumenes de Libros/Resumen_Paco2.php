      <center><h2>Paco Yunque</h2></center><br>
      <p>La historia continúa mostrando el sufrimiento de Paco en una sociedad y un sistema educativo profundamente sesgados hacia quienes ostentan el poder. Aunque Paco quiere expresar su verdad, es consciente de que no existe espacio para que alguien como él sea escuchado o defendido. Cada día en la escuela refuerza en él la idea de que la justicia no está hecha para los débiles ni para los pobres, y que su único papel en ese entorno es permanecer en silencio y obedecer. <br><br>
      Paco mantiene su dignidad a pesar de las humillaciones, y aunque su voz es ignorada, se aferra a una pequeña esperanza de ser valorado algún día. Sin embargo, el entorno hostil y las actitudes de quienes lo rodean le enseñan una dura lección: en su mundo, la bondad y el esfuerzo no son recompensados. Su regreso a casa, con la cabeza gacha y los ojos llenos de tristeza, es un reflejo de la derrota silenciosa de quienes, como él, no encuentran justicia en un sistema que solo valora a los poderosos. <br><br>
      En el desenlace de la historia, la injusticia prevalece y la situación de Paco permanece sin resolución. La trama finaliza en un tono sombrío y desolador, dejando en el lector una profunda reflexión sobre la desigualdad que enfrentan los más vulnerables. Paco regresa a su hogar, despojado de cualquier esperanza de justicia, pero con la dignidad intacta de quien ha resistido la opresión. Para Vallejo, el destino de Paco es un símbolo de las heridas que la sociedad inflige sobre los más débiles y una denuncia a un sistema que favorece a los poderosos a costa de los inocentes. <br></br>
      Con Paco Yunque, César Vallejo realiza una crítica mordaz a las estructuras de poder y al maltrato que sufren los humildes en una sociedad que desprecia a los más vulnerables. A través de la mirada inocente y triste de Paco, el autor presenta una realidad donde los esfuerzos de los desposeídos son apropiados por quienes ostentan el poder, y donde la empatía brilla por su ausencia. Así, la historia de Paco Yunque no es solo el relato de un niño abusado, sino también un espejo de la injusticia social, una invitación a reflexionar sobre el trato que recibe el débil en manos de los fuertes y una denuncia atemporal a la desigualdad en sus formas más brutales.</p>
